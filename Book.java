public class Book 
 {
  private String title;
  
  public Book(String fullTitle)
  {
    title = fullTitle;
  }
 
  public void setTitle(String fullTitle)
  {
    title = fullTitle;
  }
  
  public String getTitle()
  {
    return title;
  }
  
  public String toString()
  {
    return title;
  }
  
  
}